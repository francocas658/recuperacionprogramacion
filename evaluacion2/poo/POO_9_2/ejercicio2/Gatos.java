package evaluacion2.poo.POO_9_2.ejercicio2;

public class Gatos {

    private String nombre, sexo, color;

    public Gatos (String nombre, String sexo, String color) {
        this.nombre = nombre;
        this.sexo = sexo;
        this.color = color;
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    
    public String getSexo() {
        return this.sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    
    public String getColor() {
        return this.color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
